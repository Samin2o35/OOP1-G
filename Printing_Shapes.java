
public class Printing_Shapes {

	public static void main(String[] args) {
		 int line;
		 
		         int star;
		 
		         int row=1;
		 
		          
		 
		         for (line=0; line<3; line++) {
		
		              
		 
		             for (star=0; star<row; star++)
		
		                 System.out.print ('*');
		 
		                         System.out.println ();
		 
		                         row++;
		 
		         }

	}

}
